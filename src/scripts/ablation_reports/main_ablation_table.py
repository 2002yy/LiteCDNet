def parse_float(value: str | int | float | None) -> float | None:
    if value is None:
        return None
    text = str(value).strip()
    if not text:
        return None
    try:
        return float(text)
    except ValueError:
        return None


def fmt_float(value: object, digits: int = 2) -> str:
    if not isinstance(value, (int, float)):
        return "-"
    return f"{float(value):.{digits}f}"


def fmt_metric(value: object, digits: int = 5) -> str:
    if not isinstance(value, (int, float)):
        return "-"
    return f"{float(value):.{digits}f}"


def fmt_delta(value: object, digits: int = 5) -> str:
    if not isinstance(value, (int, float)):
        return "-"
    if abs(float(value)) < 0.5 * 10 ** (-digits):
        return "0.00000"
    return f"{float(value):+.{digits}f}"


