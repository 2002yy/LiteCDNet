def load_rows_from_summary(summary_csv: Path) -> dict[str, dict[str, object]]:
    rows_by_case: dict[str, dict[str, object]] = {}
    with summary_csv.open("r", encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            case = str(row.get("case", "")).strip()
            if not case:
                continue
            parsed: dict[str, object] = dict(row)
            for key in (
                "acc",
                "delta_acc_vs_full",
                "miou",
                "delta_miou_vs_full",
                "mf1",
                "delta_mf1_vs_full",
                "precision_1",
                "recall_1",
                "elapsed_sec",
                "elapsed_min",
                "params_m",
                "flops_g",
            ):
                parsed[key] = parse_float(row.get(key))
            rows_by_case[case] = parsed
    return rows_by_case


def load_rows_from_curve_summary(curve_summary_csv: Path) -> dict[str, dict[str, object]]:
    rows_by_case: dict[str, dict[str, object]] = {}
    with curve_summary_csv.open("r", encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            case_name = str(row.get("case_name", "")).strip()
            case = {
                "full": "full",
                "abs_diff": "abs_diff",
                "no_context": "no_context",
                "concat_decoder": "concat_decoder",
                "no_boundary": "no_boundary",
                "no_deep_supervision": "no_deep_supervision",
                "boundary_05": "boundary_05",
                "loss_weights_080604": "loss_weights_080604",
            }.get(case_name)
            if not case:
                continue
            parsed: dict[str, object] = {
                "case": case,
                "status": row.get("status", ""),
                "last_epoch": parse_float(row.get("last_epoch")),
                "max_epoch": parse_float(row.get("max_epoch")),
                "best_epoch": parse_float(row.get("best_epoch")),
                "miou": parse_float(row.get("best_val_miou")),
                "mf1": parse_float(row.get("best_val_mf1")),
                "acc": parse_float(row.get("best_val_acc")),
                "miou_last": parse_float(row.get("last_val_miou")),
                "mf1_last": parse_float(row.get("last_val_mf1")),
                "acc_last": parse_float(row.get("last_val_acc")),
            }
            rows_by_case[case] = parsed
    return rows_by_case


def load_complexity_rows(complexity_json: Path) -> dict[str, dict[str, object]]:
    data = json.loads(complexity_json.read_text(encoding="utf-8-sig"))
    return {str(row["case"]): dict(row) for row in data}


def merge_rows(args: argparse.Namespace) -> list[dict[str, object]]:
    summary_csv = resolve_summary_csv(args)
    if summary_csv and summary_csv.exists():
        perf_rows = load_rows_from_summary(summary_csv)
    else:
        curve_summary_csv = resolve_curve_summary_csv(args)
        if not curve_summary_csv.exists():
            raise FileNotFoundError(f"curve summary csv not found: {curve_summary_csv}")
        perf_rows = load_rows_from_curve_summary(curve_summary_csv)

    complexity_json = resolve_complexity_json(args)
    if not complexity_json.exists():
        raise FileNotFoundError(f"complexity json not found: {complexity_json}")
    complexity_rows = load_complexity_rows(complexity_json)

    merged_rows: list[dict[str, object]] = []
    baseline_miou = parse_float(perf_rows.get("full", {}).get("miou"))
    baseline_mf1 = parse_float(perf_rows.get("full", {}).get("mf1"))

    for case in CASE_ORDER:
        meta = CASE_META[case]
        perf = dict(perf_rows.get(case, {}))
        complexity = dict(complexity_rows.get(case, {}))

        row: dict[str, object] = {
            "case": case,
            "code": meta["code"],
            "label_cn": meta["label_cn"],
            "setting_cn": meta["setting_cn"],
            "focus_cn": meta["focus_cn"],
            "group": meta["group"],
            "status": perf.get("status", "missing"),
            "best_epoch": perf.get("best_epoch"),
            "last_epoch": perf.get("last_epoch"),
            "max_epoch": perf.get("max_epoch"),
            "miou": parse_float(perf.get("miou")),
            "mf1": parse_float(perf.get("mf1")),
            "acc": parse_float(perf.get("acc")),
            "params_m": parse_float(complexity.get("params_m")),
            "flops_g": parse_float(complexity.get("flops_g")),
            "delta_params_m_vs_full": parse_float(complexity.get("delta_params_m_vs_full")),
            "delta_flops_g_vs_full": parse_float(complexity.get("delta_flops_g_vs_full")),
        }
        row["delta_miou_vs_full"] = (
            None if row["miou"] is None or baseline_miou is None else float(row["miou"]) - baseline_miou
        )
        row["delta_mf1_vs_full"] = (
            None if row["mf1"] is None or baseline_mf1 is None else float(row["mf1"]) - baseline_mf1
        )

        if row["params_m"] is None or row["flops_g"] is None:
            complexity_fallback = compare_with_full(case)
            row["params_m"] = complexity_fallback["params_m"]
            row["flops_g"] = complexity_fallback["flops_g"]
            row["delta_params_m_vs_full"] = complexity_fallback["delta_params_m_vs_full"]
            row["delta_flops_g_vs_full"] = complexity_fallback["delta_flops_g_vs_full"]

        merged_rows.append(row)

    return merged_rows


